package com.biz.iolist.model;

public class BookVO {

	private String strName;
	private String strWriter;
	private String strComp;
	private int intPrice;
	private int intYear;
	
	String getStrName() {
		return strName;
	}
	void setStrName(String strName) {
		this.strName = strName;
	}
	String getStrWriter() {
		return strWriter;
	}
	void setStrWriter(String strWriter) {
		this.strWriter = strWriter;
	}
	String getStrComp() {
		return strComp;
	}
	void setStrComp(String strComp) {
		this.strComp = strComp;
	}
	int getIntPrice() {
		return intPrice;
	}
	void setIntPrice(int intPrice) {
		this.intPrice = intPrice;
	}
	int getIntYear() {
		return intYear;
	}
	void setIntYear(int intYear) {
		this.intYear = intYear;
	}
	
}
